<h1><b> Regulament </b></h1>
   <p style="text-align: justify; color: black; margin-left: 1cm; margin-right: 1cm">
     <br>
	 <b>Bun venit la pagina de înscriere a elevilor din Colegiul Național ,,Mihai Viteazul" Slobozia! </b>
	 <br>
	 <br>
	 <b>1. Verificarea înscrierii</b>
	 <br>
	 <br>
	 Pentru a vedea dacă ai fost înregistrat, te poți verifica la rubrica ,,Verificare înscriere", dupa CNP.
	 <br>
	 <br>
	 <b>2. Înscrierea datelor personale și a probelor</b>
	 <br>
	 <br>
	 Mergi la rubrica ,,Înscriere" și completează formularul cerut, după care mergi la rubrica ,,Înscriere probe" și adaugă datele respective. Proba la limba maternă se adaugă automat, iar proba obligatorie a profilului se adaugă tot automat, dupa înregistrarea specializării si profilului.
	 <br>
	 Pentru identificarea ID-ului, te rugăm să consulți rubrica ,,Tabel elevi".
	 Din motive de protejare a datelor personale, nu putem afișa CNP.
	 <br>
	 <br>
	 <b>3. Ștergerea înregistrărilor</b>
	 <br>
	 <br>
	 Daca ai introdus date greșite poți șterge înregistrarea, mergând la ,,Ștergere înregistrare", apoi la ,,Ștergere probe".
	 <br>
	 <br>
	 <b>4. Căutare înregistrări </b>
	 <br>
	 <br>
	 Această rubrică afișază elevii care urmează sa susțină examenul de Bacalaureat la una dintre probele la alegere.
	 <br>
	 <br>
	 <b>5. Completarea datelor </b>
	 <br>
	 <br>
	 Numele și prenumele se adaugă având prima litera a fiecăruia scrisă cu majusculă. Exemplu: Adam Ion.
	 <br>
	 Inițiala tatălui se compltează doar cu majuscule.
	 <br>
	 Clasa se completează cu cifre romane, toate clasele liceului anului acesta de a XII-a sunt: XIIA, XIIB, XIIC, XIID, XIIE, XIIF, XIIG.
	 <br>
     Profilul se completează alegând: Real sau Uman.
	 Pentru specializări, urmăriți descrierea de mai jos, în funcție de clasă, profil, pentru a completa corect.
	<ul>
    <li>Real
    <ul>
    <li> XIIA: Matematica-Informatica intensiv informatica
    <li> XIIB: Matematica-Informatica intensiv engleza
    <li> XIIC: Matematica-Informatica
    <li> XIID: Matematica-Informatica
	<li> XIIE: Stiinte ale Naturii
    </ul>
	<li>Uman
    <ul>
    <li> XIIF: Filologie
    <li> XIIG: Stiinte Sociale
    </ul>
