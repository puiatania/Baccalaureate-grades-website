 <h1>Reusitele de-a lungul anilor la examen</h1>
      <p style="text-align: justify; color: black; margin-left: 1cm; margin-right: 1cm">
	  Rezultatele de la Bacalaureat 2020 au fost publicate, iar listele arată că sunt 232 de medii de 10 la această sesiune. În total au fost înscrişi 155.639 de absolvenţi de clasa a XII-a. Acesta este cel mai mare număr de medii de 10 obţinute la Bacalaureat în ultimii 16 ani, potrivit datelor oficiale analizate de Edupedu.ro.
Anul trecut au fost 224 de medii de 10, iar în 2018 – 132.
În plus, cei care au avut în toate clasele de liceu medii generale de minimum 9,50, iar la examenul de Bacalaureat au obţinut media 10 primesc diplomă de merit.
Bonus, elevii care iau media 10 la Bacalaureat anul acesta primesc abonamente gratuite la festivalurile Neversea și UNTOLD de anul viitor, iar candidații care promovează examenul primesc vouchere de 10-20 de euro.
